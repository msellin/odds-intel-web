// ---------------------------------------------------------------------------
// Score prediction helper
// ---------------------------------------------------------------------------

function predictScore(
  hp: number | null,
  dp: number | null,
  ap: number | null
): string {
  if (hp == null || dp == null || ap == null) return "— : —";

  const strongHome = hp > 0.58;
  const strongAway = ap > 0.58;
  const drawLikely = dp > 0.27 && Math.abs(hp - ap) < 0.12;

  if (drawLikely) return "1 – 1";
  if (strongHome) return hp > 0.70 ? "3 – 0" : "2 – 0";
  if (strongAway) return ap > 0.70 ? "0 – 3" : "0 – 2";
  // Moderate favourite
  if (hp > ap) return hp > 0.50 ? "2 – 1" : "1 – 0";
  return ap > 0.50 ? "1 – 2" : "0 – 1";
}

