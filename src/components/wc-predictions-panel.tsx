// ---------------------------------------------------------------------------
// Group card
// ---------------------------------------------------------------------------

function GroupPredictionCard({
  group,
  predictions,
}: {
  group: WCGroup;
  predictions: Record<string, WCPredictionSlot>;
}) {
  // Sort fixtures by date
  const sorted = [...group.fixtures].sort(
    (a, b) => new Date(a.date).getTime() - new Date(b.date).getTime()
  );

  return (
    <div className="overflow-hidden rounded-xl border border-white/[0.08] bg-card/40">
      <header className="flex items-center gap-2 border-b border-white/[0.06] bg-white/[0.02] px-3 py-2">
        <span className="inline-flex size-5 items-center justify-center rounded-full bg-[color:var(--color-tournament-gold)]/20 text-[11px] font-bold text-[color:var(--color-tournament-gold)]">
          {group.label}
        </span>
        <span className="text-xs font-semibold text-foreground">
          Group {group.label}
        </span>
        <span className="ml-auto text-[10px] text-muted-foreground">
          {group.teams.map((t) => t.name).join(" · ")}
        </span>
      </header>

      <ol className="divide-y divide-white/[0.04]">
        {sorted.map((fixture) => {
          const pred = predictions[fixture.id];
          const hp = pred?.homeProb ?? null;
          const dp = pred?.drawProb ?? null;
          const ap = pred?.awayProb ?? null;
          const score = predictScore(hp, dp, ap);
          const hasResult =
            fixture.scoreHome != null && fixture.scoreAway != null;
          const dateStr = new Date(fixture.date).toLocaleDateString("en-GB", {
            month: "short",
            day: "numeric",
          });

          // Determine predicted winner for subtle highlighting
          let winnerSide: "home" | "draw" | "away" | null = null;
          if (hp != null && dp != null && ap != null) {
            if (hp > dp && hp > ap) winnerSide = "home";
            else if (ap > dp && ap > hp) winnerSide = "away";
            else winnerSide = "draw";
          }

          return (
            <li
              key={fixture.id}
              className="flex items-center gap-2 px-3 py-2 text-xs"
            >
              {/* Date */}
              <span className="w-10 shrink-0 text-[10px] text-muted-foreground/70">
                {dateStr}
              </span>

              {/* Home team */}
              <span
                className={`min-w-0 flex-1 truncate text-right text-[12px] ${
                  winnerSide === "home"
                    ? "font-semibold text-foreground"
                    : "text-muted-foreground"
                }`}
              >
                {fixture.home.name}
              </span>

              {/* Score pill */}
              <span
                className={`shrink-0 rounded px-2 py-0.5 text-[11px] font-bold tabular-nums ${
                  hasResult
                    ? "bg-white/[0.08] text-foreground"
                    : "bg-[color:var(--color-tournament-gold)]/15 text-[color:var(--color-tournament-gold)]"
                }`}
              >
                {hasResult
                  ? `${fixture.scoreHome} – ${fixture.scoreAway}`
                  : score}
              </span>

              {/* Away team */}
              <span
                className={`min-w-0 flex-1 truncate text-[12px] ${
                  winnerSide === "away"
                    ? "font-semibold text-foreground"
                    : "text-muted-foreground"
                }`}
              >
                {fixture.away.name}
              </span>

              {/* Prob pill — only pre-result */}
              {!hasResult && hp != null && (
                <span className="hidden shrink-0 text-[10px] text-muted-foreground/60 sm:block">
                  {Math.round((winnerSide === "away" ? ap! : winnerSide === "draw" ? dp! : hp) * 100)}%
                </span>
              )}
            </li>
          );
        })}
      </ol>
    </div>
  );
}
